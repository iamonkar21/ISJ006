package Operators.Arithmatic;

public class Area_PerimeterOfRectangle {
    public static void main(String[] args) {
        int l = 5;
        int b = 7;
        int Area = l*b;
        int Perimeter = 2*(l+b);
        System.out.println("Area of Rectangle: "+Area);
        System.out.println("Perimeter of Rectangle: "+Perimeter);

    }
}
